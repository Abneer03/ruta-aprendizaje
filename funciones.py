def calcular_area (b, h):
    return (b * h) 

print ("Calculemos el area de cualquier rectángulo")
base = int(input ("Primero dame el largo del rectángulo"))
altura = int(input("Ahora ingresa el ancho del rectángulo"))
resultado = calcular_area (base, altura) 
print ("El area del rectángulo es" ,resultado, "gracias por confiar en mi")
